#include "../includes/calc.h"
#include <math.h>

double ADD(double a, double b)
{
    return a + b;
}

double SOU(double a, double b)
{
    return a - b;
}

double MUL(double a, double b)
{
    return a * b;
}

double DIV(double a, double b)
{
    return a / b;
}

double PUI(double a, double b)
{
    return pow(a, b);
}

double MOD(double a, double b)
{
    return (int)a % (int)b;
}